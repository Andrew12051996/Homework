print('Введите шестизначный номер билета ')
s = int(input())
if ((s > 99999) & (s < 1000000)):
    a = s // 1000
    x1 = a % 10
    y1 = (a % 100) // 10
    z1 = a // 100
    summa1 =  x1 + y1 + z1
    b = s % 1000
    x2 = b % 10
    y2 = (b % 100) // 10
    z2 = b // 100
    summa2 =  x2 + y2 + z2
    if (summa1 == summa2):
        print('У вас счастливый билет')
    else:
        print('У вас самый обычный билет')
else:
    print('Номер билета не шестизначный')